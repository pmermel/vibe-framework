function ttlCleanupWrapper(p: { name: string; registryName: string; framework_repo: string }): string {
  return `name: Preview TTL Cleanup
on:
  schedule:
    - cron: '0 */6 * * *'

jobs:
  ttl-cleanup:
    uses: ${p.framework_repo}/.github/workflows/reusable-preview-ttl-cleanup.yml@v1
    with:
      resource_group: ${p.name}-rg
      preview_app_prefix: ${p.name}-pr
      registry: ${p.registryName}
      app_name: ${p.name}
      max_age_days: 2
    secrets: inherit
`;
}

function dockerfile(): string {
  return `# Multi-stage TypeScript Node API image targeting Azure Container Apps.

FROM node:20-alpine AS builder
WORKDIR /app
COPY package*.json ./
RUN npm ci
COPY . .
RUN npm run build

FROM node:20-alpine AS runner
WORKDIR /app
ENV NODE_ENV=production
ENV PORT=8080
EXPOSE 8080
COPY package*.json ./
RUN npm ci --omit=dev
COPY --from=builder /app/dist ./dist
CMD ["node", "dist/index.js"]
`;
}

