/**
 * node-api.ts
 *
 * Generates the complete file tree for a bootstrapped Node API project.
 * Returns a map of relative file path -> UTF-8 file content.
 *
 * The scaffold is intentionally minimal but fully deployable:
 * - vibe.yaml, CLAUDE.md, AGENTS.md — framework manifest and agent instructions
 * - .devcontainer/devcontainer.json — Codespaces support
 * - .github/workflows/*.yml — thin wrappers calling vibe-framework reusable workflows
 * - Dockerfile — TypeScript Node API production image (targets Azure Container Apps)
 * - package.json, tsconfig.json — Node API app config
 * - src/index.ts — minimal Express starter with /health
 *
 * Does NOT generate application business logic — agents write that after bootstrap.
 * Does NOT provision Azure resources — that is handled by configure_cloud.
 *
 * **Adapter support:** Only `"container-app"` is implemented.
 * Callers should check `adapter` before calling this function:
 * if `adapter === "static-web-app"`, `createProject` returns `not_implemented`
 * before reaching this function. The `adapter` param is accepted here so that
 * `vibe.yaml` stays provider-neutral if support expands later.
 */
export function generateNodeApiScaffold(params: {
  name: string;
  github_owner: string;
  azure_region: string;
  adapter: "container-app" | "static-web-app";
  approvers: string[];
  framework_repo: string;
}): Record<string, string> {
  const { name, github_owner, azure_region, adapter, approvers, framework_repo } = params;
  const registryName = name.replace(/-/g, "") + "acr";

  return {
    "vibe.yaml": vibeYaml({ name, github_owner, azure_region, adapter, approvers, registryName, framework_repo }),
    "CLAUDE.md": claudeMd(name),
    "AGENTS.md": agentsMd(name),
    ".ai/context/AZURE_TARGETS.md": aiContextAzureTargets({ name, github_owner, azure_region, registryName }),
    ".ai/context/STACK_DECISIONS.md": aiContextStackDecisions(name),
    ".devcontainer/devcontainer.json": devcontainer(),
    ".github/workflows/preview.yml": previewWrapper({ name, registryName, framework_repo }),
    ".github/workflows/staging.yml": stagingWrapper({ name, registryName, framework_repo }),
    ".github/workflows/production.yml": productionWrapper({ name, registryName, framework_repo }),
    ".github/workflows/preview-ttl-cleanup.yml": ttlCleanupWrapper({ name, registryName, framework_repo }),
    "Dockerfile": dockerfile(),
    "package.json": packageJson(name),
    "tsconfig.json": tsconfig(),
    "src/index.ts": indexTs(name),
    "src/__tests__/app.test.ts": appTestPlaceholder(name),
    ".gitignore": gitignore(),
    "README.md": readme({ name, github_owner, framework_repo }),
  };
}

function vibeYaml(p: {
  name: string;
  github_owner: string;
  azure_region: string;
  adapter: "container-app" | "static-web-app";
  approvers: string[];
  registryName: string;
  framework_repo: string;
}): string {
  const approversList = p.approvers.map((a) => `  - ${a}`).join("\n");
  return `# vibe.yaml — project manifest
# Generated by vibe-framework create_project. Do not edit workflow_refs manually —
# update them via bootstrap automation to keep wrapper workflows in sync.
name: ${p.name}
template: node-api
adapter: ${p.adapter}
providers:
  - claude
  - codex

branch_policy:
  feature_prefix: feature/
  staging: develop
  production: main

build:
  install: npm ci
  test: npm test
  build: npm run build
  output: dist

deploy:
  preview:
    target: container-app
    max_concurrent: 3
    ttl_hours: 48
  staging:
    target: container-app
  production:
    target: container-app

azure:
  region: ${p.azure_region}
  resource_group: ${p.name}-rg
  registry: ${p.registryName}
  container_app_environment: ${p.name}-env
  preview_app_prefix: ${p.name}-pr
  staging_app: ${p.name}-staging
  production_app: ${p.name}-prod

github:
  repo: ${p.github_owner}/${p.name}
  issues_as_work_queue: true
  workflow_refs:
    preview: ${p.framework_repo}/.github/workflows/reusable-preview.yml@v1
    staging: ${p.framework_repo}/.github/workflows/reusable-staging.yml@v1
    production: ${p.framework_repo}/.github/workflows/reusable-production.yml@v1
    preview_ttl_cleanup: ${p.framework_repo}/.github/workflows/reusable-preview-ttl-cleanup.yml@v1

approvers:
${approversList}
`;
}

