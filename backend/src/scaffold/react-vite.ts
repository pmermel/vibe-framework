function aiContextAzureTargets(p: {
  name: string;
  github_owner: string;
  azure_region: string;
}): string {
  return `# Azure Targets — ${p.name}

Project-specific Azure resource names and deployment targets. Agents must use these
values when referencing or provisioning infrastructure for this project. Do not use
framework-level resource names in project commands.

## Resource Group

| Resource | Name |
|---|---|
| Resource group | \`${p.name}-rg\` |
| Region | \`${p.azure_region}\` |

## Static Web App

| Resource | Name | Notes |
|---|---|---|
| Static Web App | \`${p.name}-swa\` | Hosts all environments (production, staging, preview) |

No Container Registry or Container Apps are used for this project. Deployment is
handled via the reusable SWA framework workflows (\`reusable-swa-*.yml\`), which fetch
the SWA deployment token at runtime via OIDC login and \`az staticwebapp secrets list\`.
No long-lived deployment token secret is stored in the repository.

## Environments

| Environment | Mechanism |
|---|---|
| Production | Main branch → SWA production slot |
| Staging | \`develop\` branch → SWA named environment \`staging\` |
| Preview | PR-based ephemeral SWA environment (auto-closed on PR close) |

## GitHub Repository

\`${p.github_owner}/${p.name}\`

## OIDC Trust

Each GitHub environment has its own Azure AD app registration and federated credential.
The OIDC token subject must match exactly for Azure login to succeed.

| GitHub Environment | OIDC Subject |
|---|---|
| \`preview\` | \`repo:${p.github_owner}/${p.name}:environment:preview\` |
| \`staging\` | \`repo:${p.github_owner}/${p.name}:environment:staging\` |
| \`production\` | \`repo:${p.github_owner}/${p.name}:environment:production\` |

OIDC federated credentials are provisioned by \`configure_cloud\` during bootstrap.
Secrets \`AZURE_CLIENT_ID\`, \`AZURE_TENANT_ID\`, and \`AZURE_SUBSCRIPTION_ID\` are stored
per GitHub environment. The SWA deployment token is fetched at runtime by the
\`reusable-swa-*.yml\` workflows via \`az staticwebapp secrets list\` — not stored as a secret.
`;
}

