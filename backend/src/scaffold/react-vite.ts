/**
 * react-vite.ts
 *
 * Generates the complete file tree for a bootstrapped React/Vite project
 * targeting Azure Static Web Apps. Returns a map of relative file path ->
 * UTF-8 file content.
 *
 * The scaffold is intentionally minimal but fully deployable:
 * - vibe.yaml, CLAUDE.md, AGENTS.md — framework manifest and agent instructions
 * - .ai/context/ — Azure targets and stack decisions (SWA-specific, no Container Registry)
 * - .devcontainer/devcontainer.json — Codespaces support (port 5173 for Vite)
 * - .github/workflows/*.yml — thin wrapper workflows calling pinned reusable SWA framework workflows
 * - index.html, vite.config.ts, tsconfig.json — Vite project config
 * - src/main.tsx, src/App.tsx, src/index.css — minimal React starter
 * - src/__tests__/App.test.ts — vitest placeholder
 * - package.json — React 18 + Vite 5 + TypeScript + vitest
 *
 * Does NOT generate application business logic — agents write that after bootstrap.
 * Does NOT provision Azure resources — that is handled by configure_cloud.
 * Does NOT generate a Dockerfile or container-related workflows — SWA deploys
 * static assets; there is no container runtime involved.
 *
 * **Adapter support:** Only `"static-web-app"` is valid for this scaffold.
 * The caller (`createProject`) enforces template + adapter compatibility before
 * reaching this function.
 */
export function generateReactViteScaffold(params: {
  name: string;
  github_owner: string;
  azure_region: string;
  adapter: "container-app" | "static-web-app";
  approvers: string[];
  framework_repo: string;
}): Record<string, string> {
  const { name, github_owner, azure_region, approvers, framework_repo } = params;

  return {
    "vibe.yaml": vibeYaml({ name, github_owner, azure_region, approvers, framework_repo }),
    "CLAUDE.md": claudeMd(name),
    "AGENTS.md": agentsMd(name),
    ".ai/context/AZURE_TARGETS.md": aiContextAzureTargets({ name, github_owner, azure_region }),
    ".ai/context/STACK_DECISIONS.md": aiContextStackDecisions(name),
    ".devcontainer/devcontainer.json": devcontainer(),
    ".github/workflows/preview.yml": previewWrapper({ name, framework_repo }),
    ".github/workflows/staging.yml": stagingWrapper({ name, framework_repo }),
    ".github/workflows/production.yml": productionWrapper({ name, framework_repo }),
    "index.html": indexHtml(name),
    "vite.config.ts": viteConfig(),
    "src/main.tsx": mainTsx(),
    "src/App.tsx": appTsx(name),
    "src/index.css": indexCss(),
    "src/__tests__/App.test.ts": appTestPlaceholder(name),
    "tsconfig.json": tsconfig(),
    "package.json": packageJson(name),
    ".gitignore": gitignore(),
    "README.md": readme({ name, github_owner, framework_repo }),
  };
}

function vibeYaml(p: {
  name: string;
  github_owner: string;
  azure_region: string;
  approvers: string[];
  framework_repo: string;
}): string {
  const approversList = p.approvers.map((a) => `  - ${a}`).join("\n");
  return `# vibe.yaml — project manifest
# Generated by vibe-framework create_project. Do not edit manually.
name: ${p.name}
template: react-vite
adapter: static-web-app
providers:
  - claude
  - codex

branch_policy:
  feature_prefix: feature/
  staging: develop
  production: main

build:
  install: npm install
  test: npm test
  build: npm run build
  output: dist

deploy:
  preview:
    target: static-web-app
  staging:
    target: static-web-app
  production:
    target: static-web-app

azure:
  region: ${p.azure_region}
  resource_group: ${p.name}-rg
  static_web_app: ${p.name}-swa

github:
  repo: ${p.github_owner}/${p.name}
  issues_as_work_queue: true
  workflow_refs:
    preview: ${p.framework_repo}/.github/workflows/reusable-swa-preview.yml@v1
    staging: ${p.framework_repo}/.github/workflows/reusable-swa-staging.yml@v1
    production: ${p.framework_repo}/.github/workflows/reusable-swa-production.yml@v1

approvers:
${approversList}
`;
}

function claudeMd(name: string): string {
  return `# ${name}

Generated project using [vibe-framework](https://github.com/pmermel/vibe-framework).

## Quick start

\`\`\`bash
npm install
npm run dev   # http://localhost:5173
npm test
\`\`\`

## Branch model

\`feature/*\` → \`develop\` (staging) → \`main\` (production, requires approval)

Always create a \`feature/<topic>\` branch before making changes. Never commit directly
to \`develop\` or \`main\`.

## Shared context

See \`.ai/context/\` for conventions, stack decisions, and Azure targets.

## Pipeline

Push to a feature branch → GitHub Actions deploys a preview to Azure Static Web Apps and
posts the URL to the PR. Merge to \`develop\` → staging updates. Approve and merge to
\`main\` → production deploys.
`;
}

