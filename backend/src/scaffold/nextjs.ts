function previewWrapper(p: { name: string; registryName: string; framework_repo: string }): string {
  return `name: Preview
on:
  pull_request:
    types: [opened, synchronize, reopened, closed]
    branches: [develop]

jobs:
  preview:
    uses: ${p.framework_repo}/.github/workflows/reusable-preview.yml@v1
    with:
      app_name: ${p.name}
      resource_group: ${p.name}-rg
      container_app_environment: ${p.name}-env
      preview_app_prefix: ${p.name}-pr
      registry: ${p.registryName}
      target_port: 3000
    secrets: inherit
`;
}

function stagingWrapper(p: { name: string; registryName: string; framework_repo: string }): string {
  return `name: Staging
on:
  push:
    branches: [develop]

jobs:
  staging:
    uses: ${p.framework_repo}/.github/workflows/reusable-staging.yml@v1
    with:
      app_name: ${p.name}
      resource_group: ${p.name}-rg
      staging_app: ${p.name}-staging
      registry: ${p.registryName}
    secrets: inherit
`;
}

function productionWrapper(p: { name: string; registryName: string; framework_repo: string }): string {
  return `name: Production
on:
  push:
    branches: [main]

jobs:
  production:
    uses: ${p.framework_repo}/.github/workflows/reusable-production.yml@v1
    with:
      app_name: ${p.name}
      resource_group: ${p.name}-rg
      production_app: ${p.name}-prod
      registry: ${p.registryName}
    secrets: inherit
`;
}

function ttlCleanupWrapper(p: { name: string; registryName: string; framework_repo: string }): string {
  return `name: Preview TTL Cleanup
on:
  schedule:
    - cron: '0 3 * * 0'  # every Sunday at 03:00 UTC

jobs:
  ttl-cleanup:
    uses: ${p.framework_repo}/.github/workflows/reusable-preview-ttl-cleanup.yml@v1
    with:
      resource_group: ${p.name}-rg
      preview_app_prefix: ${p.name}-pr
      registry: ${p.registryName}
      app_name: ${p.name}
      max_age_days: 7
    secrets: inherit
`;
}

