function claudeMd(name: string): string {
  return `# ${name}

Generated project using [vibe-framework](https://github.com/pmermel/vibe-framework).

## Quick start

\`\`\`bash
npm ci
npm run dev   # http://localhost:3000
npm test
\`\`\`

## Branch model

\`feature/*\` → \`develop\` (staging) → \`main\` (production, requires approval)

Always create a \`feature/<topic>\` branch before making changes. Never commit directly
to \`develop\` or \`main\`.

## Shared context

See \`.ai/context/\` for conventions, stack decisions, and Azure targets.

## Pipeline

Push to a feature branch → GitHub Actions deploys a preview Container App and posts
the URL to the PR. Merge to \`develop\` → staging updates. Approve and merge to
\`main\` → production deploys.
`;
}

function agentsMd(name: string): string {
  return `# ${name}

Generated project using vibe-framework.

## Branch rules
- Always work on \`feature/<topic>\` branches
- Never push directly to \`develop\` or \`main\`
- Open a PR to \`develop\` when the feature is ready

## Commit format
\`\`\`
<type>(<scope>): <short description>
\`\`\`
Types: feat, fix, docs, chore, test, refactor

## Pipeline
- Feature branch PR → preview Container App deployed → URL posted to PR
- Merge to develop → staging updated
- develop → main → production (approval required)

## Context
See \`.ai/context/\` for conventions and stack decisions.
`;
}

